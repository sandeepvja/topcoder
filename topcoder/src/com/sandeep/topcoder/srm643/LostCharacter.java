	public int[] getmins(String[] str)
	{
		String[] strCopy = Arrays.copyOf(str, str.length);
		for(int i = 0; i < strCopy.length; i++){
			updateCurrentString(strCopy, i);
			updateOtherStrings(strCopy, i);
			Arrays.sort(strCopy);
			
		}
	}
	public void updateCurrentString(String[] strArray, int elementToUpdate) {
		for(int i = 0; i < strArray.length; i++) {
			if(i != elementToUpdate) {
				strArray[i].replaceAll("?", "a");
			}
		}
	}
	public void updateOtherStrings(String[] strArray, int elementNotToUpdate){
		for(int i = 0; i < strArray.length; i++) {
			if(i != elementNotToUpdate) {
				strArray[i].replaceAll("?", "z");
			}
		}
	}
